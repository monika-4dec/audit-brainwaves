import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { summary } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get('LOVABLE_API_KEY');

    if (!LOVABLE_API_KEY) {
      throw new Error('LOVABLE_API_KEY is not configured');
    }

    const prompt = `You are an expert internal auditor analyzing audit data. Based on the following summary, provide actionable insights, identify control gaps, highlight risks, and suggest remediation steps.

Summary:
- Total Records: ${summary.total_records}
- Categories: ${summary.categories.join(', ')}
- High Risk: ${summary.high_risk} records
- Medium Risk: ${summary.medium_risk} records
- Low Risk: ${summary.low_risk} records
- Total Amount: $${summary.total_amount.toLocaleString()}
- Average AI Risk Score: ${summary.avg_risk_score}/100

Provide a comprehensive audit report with:
1. Executive Summary
2. Key Findings & Anomalies
3. Control Gaps Identified
4. Risk Assessment
5. Recommendations for Remediation
6. Forecast & Preventive Measures`;

    const response = await fetch('https://ai.gateway.lovable.dev/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${LOVABLE_API_KEY}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        model: 'google/gemini-2.5-flash',
        messages: [
          {
            role: 'system',
            content: 'You are an expert internal auditor and risk analyst. Provide detailed, actionable insights based on audit data.'
          },
          {
            role: 'user',
            content: prompt
          }
        ],
        temperature: 0.7,
        max_tokens: 2000,
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error('AI Gateway error:', response.status, errorText);
      throw new Error(`AI Gateway error: ${response.status}`);
    }

    const data = await response.json();
    const insights = data.choices[0].message.content;

    return new Response(
      JSON.stringify({ insights }),
      { headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
    );
  } catch (error: any) {
    console.error('Error generating insights:', error);
    return new Response(
      JSON.stringify({ error: error.message }),
      { status: 500, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
    );
  }
});
